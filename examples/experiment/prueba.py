# Año de nacimiento
year = 2025

# Check if the year is a leap year
# A year is a leap year if it is divisible by 4 but not divisible by 100,
# or if it is divisible by 400

if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
  print("El año es bisiesto")
